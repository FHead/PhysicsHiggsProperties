inline unsigned int FindLeptonBin( const double value, const double bins[], unsigned int nbins)
{
   unsigned int nbinboundaries = nbins+1;
   unsigned int bin = 0;
   for(unsigned int i = 0; i < nbinboundaries; i++)
   {
      if(i < nbinboundaries - 1)
      {
         if(value >= bins[i] && value < bins[i+1])
         {
            bin = i + 1;
            break;
         }
      }
      else if(i == nbinboundaries - 1)
      {
         if (value >= bins[i])
         {
            bin = nbinboundaries;
            break;
         }
      }    
   }

   return bin;
}

